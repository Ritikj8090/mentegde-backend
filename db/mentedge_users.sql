  bio TEXT,
  current_city TEXT,
  current_state TEXT,
  current_status TEXT,
  resume_link TEXT,
  portfolio_link TEXT,
  linkedin_link TEXT,
  github_link TEXT,
  hear_about TEXT,
  educations JSONB,
  experience JSONB,
  skills JSONB,
  languages JSONB,
  certificates JSONB,
  interests JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
